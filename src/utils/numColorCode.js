export function numColorCode(num) {
  switch (num) {
    case 1:
      return "#1976d2";
    case 2:
      return "#388d3c";
    case 3:
      return "#d33030";
    case 4:
      return "#7c21a2";
    case 5:
      return "#1976d2";
    case 6:
      return "#1976d2";
    default:
      return "white";
  }
}
